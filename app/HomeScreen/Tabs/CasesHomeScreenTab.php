namespace App\HomeScreen\Tabs;


use App\Baptism;
use App\Funeral;
use App\Service;
use App\Wedding;
use Carbon\Carbon;
use Illuminate\Support\Facades\Auth;

class CasesHomeScreenTab extends AbstractHomeScreenTab
{

    protected $title = 'Kasualien';
    protected $description = 'Zeigt die Kasualienplanung fÃ¼r eine Auswahl von Gemeinden';

    protected $queries = [];

    public function __construct($config = [])
    {
        $this->setDefaultConfig($config, ['includeCities' => [], 'title' => 'Kasualien']);
        parent::__construct($config);
        $this->buildQueries();
    }

    public function getTitle(): string
    {
        return $this->config['title'] ?: $this->title;
    }

    public function getContent($data = [])
    {
        foreach (['funerals', 'weddings', 'baptisms'] as $item) {
            $data[$item] = $this->queries[$item]->get();
        }
        return parent::getContent($data);
    }

    public function toArray($data = [])
    {
        foreach (['funerals', 'weddings', 'baptisms'] as $item) {
            $data[$item] = $this->queries[$item]->get();
            $data[$item.'Count'] = count($data[$item]);
        }
        return parent::toArray($data); // TODO: Change the autogenerated stub
    }


    protected function buildQueries()
    {
        $cities = (is_array($this->config['includeCities']) ? $this->config['includeCities'] : [$this->config['includeCities']]);

        $this->queries['funerals'] = Funeral::with(['service', 'service.day'])
            ->select(['funerals.*'])
            ->join('services', 'services.id', 'funerals.service_id')
            ->join('days', 'days.id', 'services.day_id')
            ->whereHas('service', function($service) use ($cities){
                $service->whereIn('city_id', $cities);
            })->orderBy('days.date', 'DESC')
            ->orderBy('time', 'DESC')
            ->limit(10);

        $this->queries['weddings'] = Wedding::with(['service', 'service.day'])
            ->select(['weddings.*', 'services.*'])
            ->join('services', 'services.id', 'weddings.service_id')
            ->join('days', 'days.id', 'services.day_id')
            ->whereHas('service', function($service) use ($cities){
                $service->whereIn('city_id', $cities);
            })->orderBy('days.date', 'DESC')
            ->orderBy('time', 'DESC')
            ->limit(10);

        $this->queries['baptisms'] = Baptism::with(['service', 'service.day'])
            ->select(['baptisms.*', 'services.*'])
            ->join('services', 'services.id', 'baptisms.service_id')
            ->join('days', 'days.id', 'services.day_id')
            ->whereHas('service', function($service) use ($cities){
                $service->whereIn('city_id', $cities);
            })->orderBy('days.date', 'DESC')
            ->orderBy('time', 'DESC')
            ->limit(10);

    }

}
